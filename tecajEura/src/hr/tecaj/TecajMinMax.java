package hr.tecaj;

public class TecajMinMax {
    private double min;
    private double max;

    public TecajMinMax() {
        super();
    }


    public void setMin(double min) {
        this.min = min;
    }

    public double getMin() {
        return min;
    }

    public void setMax(double max) {
        this.max = max;
    }

    public double getMax() {
        return max;
    }
}
